    x0[9] = 0x6520726F;
    x0[10] = 0x20657473;
    x0[11] = 0x73696170;
    // iv
    x0[12] = 0x74736F6D;
    x0[13] = 0x61206F72;

    CopyState(strdx0, x0);

    cout << "Initial State: \n";

    prntstate.x = x0;
    // prntstate.binaryform = true;
    PrintState(prntstate);

    for (int i{1}; i <= 14; ++i)
        frward.RoundFunction(x0, i);

    cout << "State after " << 14 << " rounds: \n";
    PrintState(prntstate);

    for (int i{14}; i > 0; i--)
        bckward.RoundFunction(x0, i);

    cout << "State after " << 14 << " rev. rounds: \n";
    PrintState(prntstate);
